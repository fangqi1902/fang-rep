package com.sxt.bus.utils;

public class PieChart {
	private String name;
	private Integer y;
	public PieChart(String name, Integer y) {
		super();
		this.name = name;
		this.y = y;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	
	

}
